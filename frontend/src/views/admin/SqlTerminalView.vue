<script setup>
import { ref, nextTick, onMounted } from 'vue';

const API_URL = process.env.VUE_APP_API_URL+'/api/sql-query' || 'http://localhost:3000/api/sql-query';

const currentQuery = ref('');
const history = ref([]);
const isLoading = ref(false);
const output = ref(null);
const input = ref(null);
const isConfirmationModalVisible = ref(false);
const queryToConfirm = ref('');
const isDangerousQuery = ref(false);

let historyIndex = -1;

onMounted(() => {
  input.value.focus();
});

const triggerConfirmation = () => {
  const command = currentQuery.value.trim();
  if (!command || isLoading.value) return;
  queryToConfirm.value = command;

  // Verifica se a query √© perigosa
  const dangerousPatterns = /(DROP|DELETE|TRUNCATE|ALTER|UPDATE)(?!.*WHERE)/i;
  isDangerousQuery.value = dangerousPatterns.test(command);

  isConfirmationModalVisible.value = true;
};

const handleCancel = () => {
  isConfirmationModalVisible.value = false;
  queryToConfirm.value = '';
  input.value.focus();
};

const handleConfirm = () => {
  isConfirmationModalVisible.value = false;
  executeQuery(queryToConfirm.value);
  queryToConfirm.value = '';
};

async function executeQuery(command) {
  const token = localStorage.getItem('authToken');
  if (!token) {
    history.value.push({
      id: Date.now(),
      command,
      error: '‚ùå Erro de Autentica√ß√£o: Token n√£o encontrado. Por favor, fa√ßa login novamente.'
    });
    return;
  }

  isLoading.value = true;
  const historyId = Date.now();
  history.value.push({ id: historyId, command, loading: true, showColdStartMessage: false });
  currentQuery.value = '';
  historyIndex = -1; // reseta navega√ß√£o do hist√≥rico

  await nextTick();
  output.value.scrollTop = output.value.scrollHeight;

  const coldStartTimer = setTimeout(() => {
    const historyEntry = history.value.find(h => h.id === historyId);
    if (historyEntry && historyEntry.loading) {
      historyEntry.showColdStartMessage = true;
    }
  }, 4000);

  const controller = new AbortController();
  const timeoutId = setTimeout(() => controller.abort(), 90000);

  try {
    const response = await fetch(API_URL, {
      method: 'POST',
      signal: controller.signal,
      headers: { 
        'Content-Type': 'application/json',
        'Authorization': `Bearer ${token}`
      },
      body: JSON.stringify({ query: command }),
    });

    const data = await response.json();
    const historyEntry = history.value.find(h => h.id === historyId);

    if (response.ok && data.success) {
      historyEntry.result = data.data;
    } else {
      throw new Error(data.error || `Erro HTTP ${response.status}`);
    }
  } catch (err) {
    const historyEntry = history.value.find(h => h.id === historyId);
    if (err.name === 'AbortError') {
      historyEntry.error = '‚è≥ Timeout: O pedido demorou demasiado tempo a responder.';
    } else {
      historyEntry.error = `‚ùå ${err.message}`;
    }
  } finally {
    clearTimeout(coldStartTimer);
    clearTimeout(timeoutId);
    const historyEntry = history.value.find(h => h.id === historyId);
    if(historyEntry) historyEntry.loading = false;
    isLoading.value = false;
    
    await nextTick();
    output.value.scrollTop = output.value.scrollHeight;
    input.value.focus();
  }
}

// Navega√ß√£o pelo hist√≥rico com setas ‚Üë ‚Üì
function navigateHistory(direction) {
  if (history.value.length === 0) return;

  if (historyIndex === -1) {
    historyIndex = history.value.length;
  }

  historyIndex += direction;

  if (historyIndex < 0) historyIndex = 0;
  if (historyIndex >= history.value.length) {
    historyIndex = history.value.length;
    currentQuery.value = '';
    return;
  }

  currentQuery.value = history.value[historyIndex].command;
}
</script>

